package com.sanqing.entity;

public class MerchantDo {
    private Integer id;

    private String doName;

    private String doPoint;

    private String doClass;

    private String doStatus;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getDoName() {
        return doName;
    }

    public void setDoName(String doName) {
        this.doName = doName;
    }

    public String getDoPoint() {
        return doPoint;
    }

    public void setDoPoint(String doPoint) {
        this.doPoint = doPoint;
    }

    public String getDoClass() {
        return doClass;
    }

    public void setDoClass(String doClass) {
        this.doClass = doClass;
    }

    public String getDoStatus() {
        return doStatus;
    }

    public void setDoStatus(String doStatus) {
        this.doStatus = doStatus;
    }
}